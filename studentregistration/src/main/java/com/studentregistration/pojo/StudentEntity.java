package com.studentregistration.pojo;

public class StudentEntity {
private String name;
private int regno;
private int age;
private int sid;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getRegno() {
	return regno;
}
public void setRegno(int regno) {
	this.regno = regno;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}

}
